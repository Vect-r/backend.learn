#include <stdio.h>

void main() {
   int num1 = 20, num2 = 5;

   int res1 = num1 + num2;
   printf("Add: %d\n", res1);

   int res2 = num1 - num2;
   printf("Sub: %d\n", res2);

   int res3 = num1 * num2;
   printf("Mul: %d\n", res3);

   int res4 = num1 / num2;
   printf("Div: %d\n", res4);

   int res5 = num1 % num2;
   printf("Rem: %d\n", res5);
}
